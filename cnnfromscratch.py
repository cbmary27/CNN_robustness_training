#the TTA model runs inference multiple times using randomly augmented versions
#of the same input image, then averages predictions

class TTAModel(nn.Module):
    def __init__(self, model, n_aug=5):
        super().__init__()
        self.model = model
        # makes n_aug predictions per image and then averages them up
        self.n_aug = n_aug
        self.tta_tfms = transforms.Compose([
            transforms.RandomHorizontalFlip(),
            transforms.RandomCrop(32, padding=4),
        ])

    def forward(self, x):
        logits = self.model(x)
        for _ in range(self.n_aug - 1):
            x_aug = torch.stack([self.tta_tfms(img) for img in x])
            logits = logits + self.model(x_aug)
        return logits / self.n_aug

